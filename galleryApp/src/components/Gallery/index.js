class Gallery extends Component {
  state = {ActiveId: imagesList[0].id}

  onIdChange = id => {
    this.setState({ActiveId: id})
  }

  render() {
    const {ActiveId} = this.state

    const filteredImage = imagesList.filter(
      eachImage => eachImage.id === ActiveId,
    )

    return (
      <div className="background-container">
        <div className="image-container">
          {filteredImage.map(each => (
            <img
              className="image"
              src={each.imageUrl}
              alt={each.imageAltText}
              key={each.id}
            />
          ))}
          <h1 className="heading">Nature Photography</h1>
          <p className="description">Nature Photography by Rahul</p>
          <ul className="thumbnail-list">
            {imagesList.map(eachImage => (
              <ThumbnailItem
                changeElement={this.onIdChange}
                imageDetails={eachImage}
                key={eachImage.id}
                isActive={eachImage.id === ActiveId}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
